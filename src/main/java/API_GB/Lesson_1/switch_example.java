package API_GB.Lesson_1;

public class switch_example {
    public static void main(String[] args) {
        int a=1123;
        switch (a){
            case 1:
                System.out.println("a");
                break;
            case 2:
                System.out.println("b");
                break;

            default:
                System.out.println("xxx");
                break;
        }

    }
}
